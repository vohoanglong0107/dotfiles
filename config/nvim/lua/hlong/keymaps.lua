-- Telescope
keymap("n", "<leader>ff", ":Telescope find_files<CR>", opts) -- find files
keymap("n", "<leader>fs", ":Telescope live_grep<CR>", opts) -- find string
keymap("n", "<leader>fb", ":Telescope buffers<CR>", opts) -- find buffers
keymap("n", "<leader>fo", ":Telescope lsp_document_symbols<CR>", opts) -- find symbols/object
keymap("n", "<leader>hc", ":Telescope command_history<CR>", opts) -- history commands
keymap("n", "<leader>fh", ":Telescope oldfiles<CR>", opts) -- history files
keymap("n", "<leader>fp", ":Telescope projects<CR>", opts) -- find projects

-- Nvim Tree
keymap("n", "<leader>e", ":NvimTreeToggle<CR>", opts)

-- vim-bbye
keymap("n", "<leader>q", ":Bdelete<CR>", opts)

-- Comment
keymap("n", "<leader>/", "<cmd>lua require('Comment.api').toggle.linewise.current()<CR>", opts)
